using TSOGD.PREFABS;
using UnityEngine;
namespace TSOGD.SCRIPTS { public class BrandCredit : MonoBehaviour
{
    //##### SERIALIZE FIELD PARAMETERS ###############################################################################################
    [SerializeField] private float _timerDelay = 7.0f;
    //##### SERIALIZE FIELD REFERENCES ###############################################################################################
    
    //##### SERIALIZE FIELD ARRAYS ###################################################################################################
    
    //##### TIMERS ###################################################################################################################
    private Chrono _timer;
    //##### SINGLETON ################################################################################################################
    private Views _brandCreditView;
    //##### OBJECTS ##################################################################################################################
    
    //##### OBJECTS ARRAYS ###########################################################################################################
    
    //##### REGIONS ##################################################################################################################
    #region UNITY API
    void Awake()
    {
        InitializeAwakeReferences();
    }
    void Start()
    {
        InitializeStartReferences();
    }
    void Update()
    {
            
    }
    #endregion
	//################################################################################################################################
    #region FONCTIONS D'INITIALISATION
    //##### PRIMITIVES ###############################################################################################################
    
    //################################################################################################################################
    private void InitializeAwakeReferences()
    {
        _brandCreditView = gameObject.GetComponentInParent<Views>();
        _timer = gameObject.AddComponent<Chrono>();
    }
    private void InitializeStartReferences()
    {
        InitializeTimer();
    }
    private void InitializeTimer()
    {
        if (_timer != null) {
            _timer.SetEventAction(GoToTitleView);
            _timer.Wait(_timerDelay);
        }
    }
    //################################################################################################################################
    #endregion
	//################################################################################################################################
    #region MECANIQUE DE LA CLASSE
    //##### PRIMITIVES ###############################################################################################################
    
    //################################################################################################################################
    /// <summary>
    /// Assigne la valeur ChangeView du BrandCreditView.
    /// </summary>
    private void GoToTitleView()
    {
        _brandCreditView.ChangeView = true;
    }
    //################################################################################################################################
    #endregion
	//################################################################################################################################
}}