using TSOGD.MANAGERS;
using UnityEngine;
using UnityEngine.SceneManagement;
namespace TSOGD.SCENES { public class ManagersScene : MonoBehaviour
{
    //##### SERIALIZE FIELD PARAMETERS ###############################################################################################
    
    //##### SERIALIZE FIELD REFERENCES ###############################################################################################
    
    //##### SERIALIZE FIELD ARRAYS ###################################################################################################
    
    //##### TIMERS ###################################################################################################################
    
    //##### SINGLETON ################################################################################################################
    
    //##### OBJECTS ##################################################################################################################
    
    //##### OBJECTS ARRAYS ###########################################################################################################
    MonoBehaviour[] _managers = new MonoBehaviour[1];
    //##### REGIONS ##################################################################################################################
    #region UNITY API
    void Awake()
    {
        InitializeAwakeReferences();
    }
    void Start()
    {
        InitializeStartReferences();
    }
    void Update()
    {
        ManagersSceneMecanism();
    }
    #endregion
	//################################################################################################################################
    #region FONCTIONS D'INITIALISATION
    //##### PRIMITIVES ###############################################################################################################
    
    //################################################################################################################################
    private void InitializeAwakeReferences()
    {
    
    }
    private void InitializeStartReferences()
    {
        _managers[0] = FindObjectOfType<ViewsManager>();
    }
    //################################################################################################################################
    #endregion
	//################################################################################################################################
    #region MECANIQUE DE LA CLASSE
    //##### PRIMITIVES ###############################################################################################################
    
    //################################################################################################################################
    private void ManagersSceneMecanism()
    {
        bool loadNextScene = true;
        for (int i = 0; i < _managers.Length; i++)
        {
            if (!_managers[i]) loadNextScene = false;
        }
        if (loadNextScene) SceneManager.LoadScene("DatasScene");
    }    
    //################################################################################################################################
    #endregion
	//################################################################################################################################
}}